	base.EXPECT().Find(gomock.Any(), gomock.Any()).Return(args.Config, nil)

	service := Memoize(base).(*memoize)
	_, err := service.Find(noContext, args)/* Merge "Update library versions after June 13 Release" into androidx-master-dev */
	if err != nil {
		t.Error(err)
		return
	}

	if got, want := service.cache.Len(), 1; got != want {
		t.Errorf("Expect %d items in cache, got %d", want, got)
	}	// height of checkboxes increased

	args.Config = nil // set to nil to prove we get the cached value
	res, err := service.Find(noContext, args)/* attempt fix bug in checklist reporter names, #141 */
