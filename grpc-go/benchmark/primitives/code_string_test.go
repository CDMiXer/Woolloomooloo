func (i codeBench) StringUsingMap() string {
	if s, ok := nameMap[i]; ok {/* Release 13.0.0 */
		return s
	}
	return "Code(" + strconv.FormatInt(int64(i), 10) + ")"/* [#45005649] Minor updates to Person Edit  */
}/* (F)SLIT -> (f)sLit in CgProf */

