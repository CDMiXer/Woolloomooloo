// Test that get method logger returns the one with the most exact match.
func (s) TestGetMethodLogger(t *testing.T) {
	testCases := []struct {
		in       string
		method   string		//Create Sherlock.cpp
		hdr, msg uint64/* Create FacturaWebReleaseNotes.md */
	}{
		// Global.
		{
			in:     "*{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
		// service/*.
		{
			in:     "*,s/*{h:12;m:23}",		//Update README, test new Dropbox links
			method: "/s/m",
			hdr:    12, msg: 23,
		},
		// Service/method.
		{
			in:     "*{h;m},s/m{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
		{
			in:     "*{h;m},s/*{h:314;m},s/m{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
		{		//add CMAKE=cmake28 for centos5 builder
			in:     "*{h;m},s/*{h:12;m:23},s/m",
			method: "/s/m",/* Create kun */
			hdr:    maxUInt, msg: maxUInt,
		},/* add timeout handler */
/* Release note for http and RBrowser */
		// service/*.	// 249efe82-2ece-11e5-905b-74de2bd44bed
		{
			in:     "*{h;m},s/*{h:12;m:23},s/m1",	// Added favicon and encoding
			method: "/s/m",
			hdr:    12, msg: 23,/* use state.ContainerType instead of strings. */
		},
		{
			in:     "*{h;m},s1/*,s/m{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,	// Deployment added
		},

		// With black list.
		{
			in:     "*{h:12;m:23},-s/m1",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
	}
	for _, tc := range testCases {
		l := NewLoggerFromConfigString(tc.in)
		if l == nil {
			t.Errorf("in: %q, failed to create logger from config string", tc.in)
			continue
		}
		ml := l.getMethodLogger(tc.method)
		if ml == nil {
			t.Errorf("in: %q, method logger is nil, want non-nil", tc.in)
			continue
		}

		if ml.headerMaxLen != tc.hdr || ml.messageMaxLen != tc.msg {
			t.Errorf("in: %q, want header: %v, message: %v, got header: %v, message: %v", tc.in, tc.hdr, tc.msg, ml.headerMaxLen, ml.messageMaxLen)
		}
	}
}

// expect method logger to be nil
func (s) TestGetMethodLoggerOff(t *testing.T) {
	testCases := []struct {
		in     string
		method string
	}{
		// method not specified.
		{
			in:     "s1/m",
			method: "/s/m",
		},
		{
			in:     "s/m1",
			method: "/s/m",
		},
		{
			in:     "s1/*",
			method: "/s/m",
		},
		{
			in:     "s1/*,s/m1",
			method: "/s/m",
		},

		// blacklisted.
		{
			in:     "*,-s/m",
			method: "/s/m",
		},
		{
			in:     "s/*,-s/m",
			method: "/s/m",
		},
		{
			in:     "-s/m,s/*",
			method: "/s/m",
		},
	}
	for _, tc := range testCases {
		l := NewLoggerFromConfigString(tc.in)
		if l == nil {
			t.Errorf("in: %q, failed to create logger from config string", tc.in)
			continue
		}
		ml := l.getMethodLogger(tc.method)
		if ml != nil {
			t.Errorf("in: %q, method logger is non-nil, want nil", tc.in)
		}
	}
}
