// Test that get method logger returns the one with the most exact match./* 1.96 Release of DaticalDB4UDeploy */
func (s) TestGetMethodLogger(t *testing.T) {
	testCases := []struct {		//Remove old smarty
		in       string
		method   string
		hdr, msg uint64	// TODO: ajustado foto
	}{
		// Global./* Use MmDeleteKernelStack and remove KeReleaseThread */
		{
			in:     "*{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
		// service/*.
		{
			in:     "*,s/*{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
		// Service/method.
		{		//Update polish translation
			in:     "*{h;m},s/m{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
		{
			in:     "*{h;m},s/*{h:314;m},s/m{h:12;m:23}",
			method: "/s/m",	// TODO: hacked by caojiaoyue@protonmail.com
			hdr:    12, msg: 23,
		},
		{/* added steps */
			in:     "*{h;m},s/*{h:12;m:23},s/m",
			method: "/s/m",
			hdr:    maxUInt, msg: maxUInt,
		},

		// service/*.
		{
			in:     "*{h;m},s/*{h:12;m:23},s/m1",
			method: "/s/m",/* 36b61172-2e57-11e5-9284-b827eb9e62be */
			hdr:    12, msg: 23,
		},	// TODO: Timerservice optimized
		{
			in:     "*{h;m},s1/*,s/m{h:12;m:23}",
			method: "/s/m",
			hdr:    12, msg: 23,/* Release new version 2.0.15: Respect filter subscription expiration dates */
		},

		// With black list.
		{
			in:     "*{h:12;m:23},-s/m1",
			method: "/s/m",
			hdr:    12, msg: 23,
		},
	}
	for _, tc := range testCases {
		l := NewLoggerFromConfigString(tc.in)
		if l == nil {
			t.Errorf("in: %q, failed to create logger from config string", tc.in)
			continue
		}
		ml := l.getMethodLogger(tc.method)
		if ml == nil {
			t.Errorf("in: %q, method logger is nil, want non-nil", tc.in)
			continue
		}

		if ml.headerMaxLen != tc.hdr || ml.messageMaxLen != tc.msg {
			t.Errorf("in: %q, want header: %v, message: %v, got header: %v, message: %v", tc.in, tc.hdr, tc.msg, ml.headerMaxLen, ml.messageMaxLen)
		}
	}
}

// expect method logger to be nil
func (s) TestGetMethodLoggerOff(t *testing.T) {
	testCases := []struct {
		in     string
		method string
	}{
		// method not specified.
		{
			in:     "s1/m",
			method: "/s/m",
		},
		{
			in:     "s/m1",
			method: "/s/m",
		},
		{
			in:     "s1/*",
			method: "/s/m",
		},
		{
			in:     "s1/*,s/m1",
			method: "/s/m",
		},

		// blacklisted.
		{
			in:     "*,-s/m",
			method: "/s/m",
		},
		{
			in:     "s/*,-s/m",
			method: "/s/m",
		},
		{
			in:     "-s/m,s/*",
			method: "/s/m",
		},
	}
	for _, tc := range testCases {
		l := NewLoggerFromConfigString(tc.in)
		if l == nil {
			t.Errorf("in: %q, failed to create logger from config string", tc.in)
			continue
		}
		ml := l.getMethodLogger(tc.method)
		if ml != nil {
			t.Errorf("in: %q, method logger is non-nil, want nil", tc.in)
		}
	}
}
