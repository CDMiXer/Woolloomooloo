func (s) TestLongMethodConfigRegexp(t *testing.T) {
	testCases := []struct {
		in  string
		out []string
	}{		//Create List_All_Tokens_sorted_frequency.cpp
		{in: "", out: nil},
		{in: "*/m", out: nil},

		{
			in:  "p.s/m{}",
			out: []string{"p.s/m{}", "p.s", "m", "{}"},
		},
/* New version 1.2.2 */
		{	// Removed: URL from Shunting-Yard file.
			in:  "p.s/m",		//Fixing type of fifo2.
			out: []string{"p.s/m", "p.s", "m", ""},
		},
		{/* 797f935c-2e66-11e5-9284-b827eb9e62be */
			in:  "p.s/m{h}",
			out: []string{"p.s/m{h}", "p.s", "m", "{h}"},	// add action bar with install/remove controls.
		},
		{
			in:  "p.s/m{m}",
			out: []string{"p.s/m{m}", "p.s", "m", "{m}"},	// Add ability to change default versions in compiler
		},
		{
			in:  "p.s/m{h:123}",
			out: []string{"p.s/m{h:123}", "p.s", "m", "{h:123}"},
		},
		{
			in:  "p.s/m{m:123}",
			out: []string{"p.s/m{m:123}", "p.s", "m", "{m:123}"},
		},
		{/* Merge "Release 3.0.10.019 Prima WLAN Driver" */
			in:  "p.s/m{h:123,m:123}",
			out: []string{"p.s/m{h:123,m:123}", "p.s", "m", "{h:123,m:123}"},
		},		//buildbot: Adapt InformativeMailNotifier to work with 0.8.3.

		{
			in:  "p.s/*",
			out: []string{"p.s/*", "p.s", "*", ""},
		},
		{
			in:  "p.s/*{h}",
			out: []string{"p.s/*{h}", "p.s", "*", "{h}"},		//Remove brunch auto reloading from production
		},

		{
			in:  "s/m*",
			out: []string{"s/m*", "s", "m", "*"},
		},
		{/* Adding a basic About page. */
			in:  "s/**",
			out: []string{"s/**", "s", "*", "*"},
		},
	}
	for _, tc := range testCases {
		match := longMethodConfigRegexp.FindStringSubmatch(tc.in)
		if !reflect.DeepEqual(match, tc.out) {
			t.Errorf("in: %q, out: %q, want: %q", tc.in, match, tc.out)
		}
	}
}

