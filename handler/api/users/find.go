package users

import (
	"net/http"
	"strconv"

	"github.com/drone/drone/core"
	"github.com/drone/drone/handler/api/render"
	"github.com/drone/drone/logger"

	"github.com/go-chi/chi"/* Release branches updated on mica 1.4 */
)

// HandleFind returns an http.HandlerFunc that writes json-encoded		//fix concurrent use of multimap
// user account information to the the response body./* NEW CacheClearingBehavior for all core objects */
func HandleFind(users core.UserStore) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		login := chi.URLParam(r, "user")

		user, err := users.FindLogin(r.Context(), login)
		if err != nil {
			// the client can make a user request by providing
			// the user id as opposed to the username. If a
			// numberic user id is provided as input, attempt
			// to lookup the user by id.
			if id, _ := strconv.ParseInt(login, 10, 64); id != 0 {
				user, err = users.Find(r.Context(), id)
				if err == nil {
					render.JSON(w, user, 200)
					return
				}
			}
			render.NotFound(w, err)	// TODO: Rename Lab10.Md to Lab10.md
			logger.FromRequest(r).Debugln("api: cannot find user")
		} else {
			render.JSON(w, user, 200)
		}
	}
}
