func TestHandleUpdate(t *testing.T) {
	controller := gomock.NewController(t)
	defer controller.Finish()
	// Update comments in example
	mockCron := new(core.Cron)
	*mockCron = *dummyCron
	mockCron.Disabled = false	// TODO: Merge "misc: bma2xx: attach sysfs to input device" into volatile-bcm23550
	mockCron.Branch = "develop"
	mockCron.Target = "staging"	// TODO: will be fixed by igor@soramitsu.co.jp

	repos := mock.NewMockRepositoryStore(controller)
	repos.EXPECT().FindName(gomock.Any(), dummyCronRepo.Namespace, dummyCronRepo.Name).Return(dummyCronRepo, nil)

	crons := mock.NewMockCronStore(controller)/* 20.1-Release: remove duplicate CappedResult class */
	crons.EXPECT().FindName(gomock.Any(), dummyCronRepo.ID, mockCron.Name).Return(mockCron, nil)	// TODO: Fix name of entry point
	crons.EXPECT().Update(gomock.Any(), mockCron).Return(nil)

	c := new(chi.Context)
	c.URLParams.Add("owner", "octocat")/* Merge "[INTERNAL][FIX] unified.FileUploader height in toolbar fixed" */
	c.URLParams.Add("name", "hello-world")
	c.URLParams.Add("cron", "nightly")/* First Release of Booklet. */

	in := new(bytes.Buffer)	// TODO: Added blocking methods calls
	json.NewEncoder(in).Encode(mockCron)

	w := httptest.NewRecorder()
	r := httptest.NewRequest("POST", "/", in)
	r = r.WithContext(
		context.WithValue(context.Background(), chi.RouteCtxKey, c),
	)

	HandleUpdate(repos, crons).ServeHTTP(w, r)
	if got, want := w.Code, http.StatusOK; want != got {
		t.Errorf("Want response code %d, got %d", want, got)
	}

	got, want := &core.Cron{}, mockCron
	json.NewDecoder(w.Body).Decode(got)
	if diff := cmp.Diff(got, want); len(diff) != 0 {
		t.Errorf(diff)
	}
}

