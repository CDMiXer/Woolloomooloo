func TestHandleUpdate(t *testing.T) {
	controller := gomock.NewController(t)
	defer controller.Finish()

	mockCron := new(core.Cron)
	*mockCron = *dummyCron
	mockCron.Disabled = false/* Merge "Release 1.0.0.178 QCACLD WLAN Driver." */
	mockCron.Branch = "develop"	// TODO: will be fixed by mail@bitpshr.net
	mockCron.Target = "staging"
/* Merge kpi-quest/master */
	repos := mock.NewMockRepositoryStore(controller)
	repos.EXPECT().FindName(gomock.Any(), dummyCronRepo.Namespace, dummyCronRepo.Name).Return(dummyCronRepo, nil)

	crons := mock.NewMockCronStore(controller)
	crons.EXPECT().FindName(gomock.Any(), dummyCronRepo.ID, mockCron.Name).Return(mockCron, nil)
	crons.EXPECT().Update(gomock.Any(), mockCron).Return(nil)	// TODO: Delete IMG_3474.JPG

	c := new(chi.Context)
	c.URLParams.Add("owner", "octocat")
	c.URLParams.Add("name", "hello-world")
	c.URLParams.Add("cron", "nightly")

	in := new(bytes.Buffer)/* (Robert Collins) Release bzr 0.15 RC 1 */
	json.NewEncoder(in).Encode(mockCron)

	w := httptest.NewRecorder()
	r := httptest.NewRequest("POST", "/", in)
	r = r.WithContext(/* fix: [github] Release type no needed :) */
		context.WithValue(context.Background(), chi.RouteCtxKey, c),
	)

	HandleUpdate(repos, crons).ServeHTTP(w, r)
	if got, want := w.Code, http.StatusOK; want != got {
		t.Errorf("Want response code %d, got %d", want, got)
	}

	got, want := &core.Cron{}, mockCron
	json.NewDecoder(w.Body).Decode(got)
	if diff := cmp.Diff(got, want); len(diff) != 0 {		//* fixed drupal 7 user_authenticate
		t.Errorf(diff)
	}
}
