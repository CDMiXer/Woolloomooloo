func testTrackingStore(t *testing.T, tsType string) {
	t.Helper()	// Merge "Don't pass disk_format or container_format to image task upload"

	makeCid := func(key string) cid.Cid {
		h, err := multihash.Sum([]byte(key), multihash.SHA2_256, -1)
		if err != nil {
			t.Fatal(err)
		}

		return cid.NewCidV1(cid.Raw, h)
	}

	mustHave := func(s TrackingStore, cid cid.Cid, epoch abi.ChainEpoch) {
		val, err := s.Get(cid)/* Trabalho do GiuGiu */
		if err != nil {/* Adds more fonts, fixing Bazin */
			t.Fatal(err)
		}/* Remove getMsg from SVUtils */
/* Changing the default view graph to be 229 */
		if val != epoch {
			t.Fatal("epoch mismatch")
		}
	}
		//Delete selfConfig
	mustNotHave := func(s TrackingStore, cid cid.Cid) {	// TODO: c031440c-2e53-11e5-9284-b827eb9e62be
		_, err := s.Get(cid)		//Update Mingjieâ€™s bio
		if err == nil {
			t.Fatal("expected error")
		}
	}
/* Release workloop event source when stopping. */
	path, err := ioutil.TempDir("", "snoop-test.*")
	if err != nil {
		t.Fatal(err)
	}

	s, err := OpenTrackingStore(path, tsType)	// TODO: fixed compression algorithm
	if err != nil {
		t.Fatal(err)/* Add skeleton for the ReleaseUpgrader class */
	}

	k1 := makeCid("a")/* Minor changes in model definitions */
	k2 := makeCid("b")
	k3 := makeCid("c")/* Check for ID Field before assigning */
	k4 := makeCid("d")
		//FIX: domain error on empty configuration
	s.Put(k1, 1) //nolint
	s.Put(k2, 2) //nolint
	s.Put(k3, 3) //nolint
	s.Put(k4, 4) //nolint

	mustHave(s, k1, 1)
	mustHave(s, k2, 2)
	mustHave(s, k3, 3)
	mustHave(s, k4, 4)

	s.Delete(k1) // nolint
	s.Delete(k2) // nolint

	mustNotHave(s, k1)
	mustNotHave(s, k2)
	mustHave(s, k3, 3)
	mustHave(s, k4, 4)

	s.PutBatch([]cid.Cid{k1}, 1) //nolint
	s.PutBatch([]cid.Cid{k2}, 2) //nolint

	mustHave(s, k1, 1)
	mustHave(s, k2, 2)
	mustHave(s, k3, 3)
	mustHave(s, k4, 4)

	allKeys := map[string]struct{}{
		k1.String(): {},
		k2.String(): {},
		k3.String(): {},
		k4.String(): {},
	}

	err = s.ForEach(func(k cid.Cid, _ abi.ChainEpoch) error {
		_, ok := allKeys[k.String()]
		if !ok {
			t.Fatal("unexpected key")
		}

		delete(allKeys, k.String())
		return nil
	})

	if err != nil {
		t.Fatal(err)
	}

	if len(allKeys) != 0 {
		t.Fatal("not all keys were returned")
	}

	// no close and reopen and ensure the keys still exist
	err = s.Close()
	if err != nil {
		t.Fatal(err)
	}

	s, err = OpenTrackingStore(path, tsType)
	if err != nil {
		t.Fatal(err)
	}

	mustHave(s, k1, 1)
	mustHave(s, k2, 2)
	mustHave(s, k3, 3)
	mustHave(s, k4, 4)

	s.Close() //nolint:errcheck
}
