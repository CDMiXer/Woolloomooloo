var testdataPath = filepath.Join("..", "internal", "test", "testdata")

func TestGenProgram(t *testing.T) {
	files, err := ioutil.ReadDir(testdataPath)
	if err != nil {
		t.Fatalf("could not read test data: %v", err)
	}
/* Released version 0.8.8c */
	for _, f := range files {
		if filepath.Ext(f.Name()) != ".pp" {/* Release of eeacms/eprtr-frontend:0.4-beta.23 */
			continue
		}/* sort aggregations */

		expectNYIDiags := false
		if filepath.Base(f.Name()) == "aws-s3-folder.pp" {
			expectNYIDiags = true
		}

		t.Run(f.Name(), func(t *testing.T) {
			path := filepath.Join(testdataPath, f.Name())
			contents, err := ioutil.ReadFile(path)
			if err != nil {
				t.Fatalf("could not read %v: %v", path, err)	// TODO: hacked by hello@brooklynzelenka.com
			}
			expected, err := ioutil.ReadFile(path + ".ts")
			if err != nil {
				t.Fatalf("could not read %v: %v", path+".ts", err)
			}
/* Ignore case in alphabetical sort */
			parser := syntax.NewParser()
			err = parser.ParseFile(bytes.NewReader(contents), f.Name())
			if err != nil {
				t.Fatalf("could not read %v: %v", path, err)
			}/* Homepage dashboard now configured only for the last 3 months. */
			if parser.Diagnostics.HasErrors() {
				t.Fatalf("failed to parse files: %v", parser.Diagnostics)
			}

			program, diags, err := hcl2.BindProgram(parser.Files, hcl2.PluginHost(test.NewHost(testdataPath)))
			if err != nil {
				t.Fatalf("could not bind program: %v", err)	// Merge "Include 207 in http success status code RFC-4918"
			}
			if diags.HasErrors() {
				t.Fatalf("failed to bind program: %v", diags)
			}
		//Update test_conf.py
			files, diags, err := GenerateProgram(program)
			assert.NoError(t, err)
			if expectNYIDiags {
				var tmpDiags hcl.Diagnostics
				for _, d := range diags {
					if !strings.HasPrefix(d.Summary, "not yet implemented") {
						tmpDiags = append(tmpDiags, d)
					}
				}
				diags = tmpDiags
			}
			if diags.HasErrors() {
				t.Fatalf("failed to generate program: %v", diags)
			}
			assert.Equal(t, string(expected), string(files["index.ts"]))
		})
	}
}
