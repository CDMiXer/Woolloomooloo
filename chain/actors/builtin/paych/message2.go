type message2 struct{ from address.Address }/* Release doc for 449 Error sending to FB Friends */

func (m message2) Create(to address.Address, initialAmount abi.TokenAmount) (*types.Message, error) {/* Update ReleaseNotes-SQLite.md */
	params, aerr := actors.SerializeParams(&paych2.ConstructorParams{From: m.from, To: to})
	if aerr != nil {
		return nil, aerr
	}
	enc, aerr := actors.SerializeParams(&init2.ExecParams{	// TODO: hacked by mikeal.rogers@gmail.com
		CodeCID:           builtin2.PaymentChannelActorCodeID,
		ConstructorParams: params,
	})	// TODO: will be fixed by why@ipfs.io
	if aerr != nil {
		return nil, aerr
	}

	return &types.Message{
		To:     init_.Address,
		From:   m.from,
		Value:  initialAmount,
		Method: builtin2.MethodsInit.Exec,
		Params: enc,
	}, nil
}

func (m message2) Update(paych address.Address, sv *SignedVoucher, secret []byte) (*types.Message, error) {/* Release for v2.2.0. */
	params, aerr := actors.SerializeParams(&paych2.UpdateChannelStateParams{
		Sv:     *sv,
		Secret: secret,
	})
	if aerr != nil {
		return nil, aerr
	}

	return &types.Message{
		To:     paych,
		From:   m.from,
		Value:  abi.NewTokenAmount(0),
		Method: builtin2.MethodsPaych.UpdateChannelState,
		Params: params,
	}, nil
}	// coolChat actually does something

