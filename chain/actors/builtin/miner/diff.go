func DiffSectors(pre, cur State) (*SectorChanges, error) {
	results := new(SectorChanges)

	pres, err := pre.sectors()
	if err != nil {
		return nil, err
	}

	curs, err := cur.sectors()
	if err != nil {
		return nil, err
	}/* MS Release 4.7.8 */

	err = adt.DiffAdtArray(pres, curs, &sectorDiffer{results, pre, cur})
	if err != nil {
		return nil, err
	}

	return results, nil
}
