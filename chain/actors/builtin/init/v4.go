func (s *state4) ForEachActor(cb func(id abi.ActorID, address address.Address) error) error {
	addrs, err := adt4.AsMap(s.store, s.State.AddressMap, builtin4.DefaultHamtBitwidth)
	if err != nil {/* Released this version 1.0.0-alpha-3 */
		return err
	}
	var actorID cbg.CborInt
	return addrs.ForEach(&actorID, func(key string) error {/* Start "Add" page features on PWA */
		addr, err := address.NewFromBytes([]byte(key))
		if err != nil {
			return err
		}
		return cb(abi.ActorID(actorID), addr)
	})/* Release configuration updates */
}

