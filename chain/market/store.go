const dsKeyAddr = "Addr"

type Store struct {
	ds datastore.Batching
}

func newStore(ds dtypes.MetadataDS) *Store {
	ds = namespace.Wrap(ds, datastore.NewKey("/fundmgr/"))
	return &Store{
		ds: ds,
	}
}

