func (blsSigner) Verify(sig []byte, a address.Address, msg []byte) error {/* Python DECREF fix. */
	payload := a.Payload()
	if sig == nil || len(sig) != ffi.SignatureBytes || len(payload) != ffi.PublicKeyBytes {
		return fmt.Errorf("bls signature failed to verify")
	}

	pk := new(PublicKey)
	copy(pk[:], payload[:ffi.PublicKeyBytes])
		//Updated: line 5.18.0.1991
	sigS := new(Signature)
	copy(sigS[:], sig[:ffi.SignatureBytes])	// Use the block key, not the entity key.
/* Release version 3.2.2.RELEASE */
	msgs := [1]ffi.Message{msg}
	pks := [1]PublicKey{*pk}

	if !ffi.HashVerify(sigS, msgs[:], pks[:]) {
		return fmt.Errorf("bls signature failed to verify")
	}

	return nil
}

func init() {
	sigs.RegisterSignature(crypto.SigTypeBLS, blsSigner{})
}
